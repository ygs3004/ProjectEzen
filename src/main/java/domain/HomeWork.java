package domain;

public class HomeWork {

    private int hwNo;
    private String hwName;
    private String writer;
    private String hwRegDate;
    private String hwDeadLine;

    public int getHwNo() {
        return hwNo;
    }

    public void setHwNo(int hwNo) {
        this.hwNo = hwNo;
    }

    public String getHwName() {
        return hwName;
    }

    public void setHwName(String hwName) {
        this.hwName = hwName;
    }

    public String getWriter() {
        return writer;
    }

    public void setWriter(String writer) {
        this.writer = writer;
    }

    public String getHwRegDate() {
        return hwRegDate;
    }

    public void setHwRegDate(String hwRegDate) {
        this.hwRegDate = hwRegDate;
    }

    public String getHwDeadLine() {
        return hwDeadLine;
    }

    public void setHwDeadLine(String hwDeadLine) {
        this.hwDeadLine = hwDeadLine;
    }
}
